document.addEventListener('DOMContentLoaded', function() {
    const citySelect = document.getElementById('citySelect');
    const districtSelect = document.getElementById('districtSelect');
    
    Object.keys(cities).forEach(city => {
        const option = document.createElement('option');
        option.value = city;
        option.textContent = city;
        if ('{{ request("city") }}' === city) option.selected = true;
        citySelect.appendChild(option);
    });
    
});

function loadDistricts() {
    const citySelect = document.getElementById('citySelect');
    const districtSelect = document.getElementById('districtSelect');
    const selectedCity = citySelect.value;
    
    districtSelect.innerHTML = '<option value="">İlçe Seçin</option>';
    
    if (selectedCity && cities[selectedCity]) {
        cities[selectedCity].forEach(district => {
            const option = document.createElement('option');
            option.value = district;
            option.textContent = district;
            districtSelect.appendChild(option);
        });
    }
    
    if ('{{ request("city") }}') {
        loadDistricts();
        if ('{{ request("location") }}') {
            setTimeout(() => {
                districtSelect.value = '{{ request("location") }}';
            }, 100);
        }
    }
}

function toggleFavorite(jobId) {
    fetch(`/jobs/${jobId}/favorite`, {
        method: 'POST',
        headers: {
            'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content'),
            'Content-Type': 'application/json',
        }
    })
    .then(response => response.json())
    .then(data => {
        const heartButton = document.querySelector(`button[onclick="toggleFavorite(${jobId})"]`);
        if (data.favorited) {
            heartButton.classList.add('favorited');
        } else {
            heartButton.classList.remove('favorited');
        }
    })
    .catch(error => {
        console.error('Error:', error);
    });
}
